psaExt.getHolidaySet = async function () {
  try {
    const { holidays } = await chrome.storage.local.get("holidays");
    if (Array.isArray(holidays) && holidays.length > 0) return new Set(holidays);
  } catch (err) {
    console.warn("[psa-ext] holidays storage read failed", err);
  }
  return new Set(psaExt.HARDCODED_JP_HOLIDAYS);
};
